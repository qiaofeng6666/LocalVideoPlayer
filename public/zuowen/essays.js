// 动态生成作文列表
document.addEventListener('DOMContentLoaded', () => {
  const container = document.getElementById('essays-container');

  essays.forEach((essay, index) => {
    const essayCard = document.createElement('article');
    essayCard.className = 'essay-card';

    // ✅ 核心：用 \n 分割段落，并过滤空行
    const paragraphs = essay.content
      .split('\n')
      .filter(para => para.trim() !== '')
      .map(para => `<p>${para.trim()}</p>`)
      .join('');

    essayCard.innerHTML = `
      <div class="essay-header">
        <h2>${essay.title}</h2>
        <span class="author">—— ${essay.author}</span>
      </div>
      <div class="essay-content" id="content-${index}" style="display: none;">
        ${paragraphs}
      </div>
      <button class="toggle-btn" onclick="toggleContent(${index})">
        阅读全文
      </button>
    `;

    container.appendChild(essayCard);
  });
});

// 控制展开/收起
function toggleContent(index) {
  const content = document.getElementById(`content-${index}`);
  const button = event.target;

  if (content.style.display === 'none') {
    content.style.display = 'block';
    button.textContent = '收起';
  } else {
    content.style.display = 'none';
    button.textContent = '阅读全文';
  }
}